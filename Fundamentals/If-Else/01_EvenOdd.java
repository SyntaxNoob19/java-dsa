// Write a Program to Check Whether Number is Even or Odd

class EvenOdd
{
    public static void main(String []args)
    {
        int num = 10;
        if(num % 2 == 0)
        {
            System.out.println("The number is even.");
        }
        
        else
        {
            System.out.println("The number is odd.");
        }
    }
}
